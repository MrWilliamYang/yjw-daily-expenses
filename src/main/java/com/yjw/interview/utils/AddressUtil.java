    /**
     * 将传入的省市区code按照  - 拆分，得到code数组
     * 
     * @param code
     * @return
     */
    public static String[] spiltCode(String code) {
        String[] spiltCode = code.split("-");
        return spiltCode;
    }

    /**
     * 根据拆分出来的code数组，得到对应省市区code
     * 
     * @param code
     * @return
     */
    public static String getCode(String[] spiltCode) {
        String provinceName = new String();
        String cityName = new String();
        String areaName = new String();
        String name = new String();
        //判断得到省市区还是省市
        //得到省市区
        if (spiltCode.length == PROVINCE_CITY_AREA) {
            String province = spiltCode[0];
            String city = spiltCode[1];
            String area = spiltCode[2];
            for (String provinceKey : provinceMap.keySet()) {
                if (provinceKey.equals(province)) {
                    provinceName = provinceMap.get(provinceKey);
                }
            }
            for (String cityKey : cityMap.keySet()) {
                if (cityKey.equals(city)) {
                    cityName = cityMap.get(cityKey);
                }
            }
            for (String areaKey : areaMap.keySet()) {
                if (areaKey.equals(area)) {
                    areaName = areaMap.get(areaKey);
                }
            }
            name = provinceName + cityName + areaName;
            //得到省市
        } else if (spiltCode.length == PROVINCE_CITY) {
            String province = spiltCode[0];
            String city = spiltCode[1];

            for (String provinceKey : provinceMap.keySet()) {
                if (provinceKey.equals(province)) {
                    provinceName = provinceMap.get(provinceKey);
                }
            }
            for (String cityKey : cityMap.keySet()) {
                if (cityKey.equals(city)) {
                    cityName = cityMap.get(cityKey);
                }
            }
            name = provinceName + cityName;
        }
        return name;
    }

    /**
     * 经过转换得到名字
     * 
     * @param code
     * @return
     */
    public static String codeToChinese(String code) {
        //拆code
        String[] spiltCode = spiltCode(code);
        //根据code匹配名字
        String name = getCode(spiltCode);
        return name;
    }

